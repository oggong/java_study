package model.vo;

public class Stock {

	String ONO;
	String ONAME;
	int OCOUNT;
	public String getONO() {
		return ONO;
	}
	public void setONO(String oNO) {
		ONO = oNO;
	}
	public String getONAME() {
		return ONAME;
	}
	public void setONAME(String oNAME) {
		ONAME = oNAME;
	}
	public int getOCOUNT() {
		return OCOUNT;
	}
	public void setOCOUNT(int oCOUNT) {
		OCOUNT = oCOUNT;
	}
	
}
